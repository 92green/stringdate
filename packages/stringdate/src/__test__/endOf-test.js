// @flow
import endOf from '../endOf';

describe('endOf', () => {

    test.each([
        ['year', '2020-01-10T22:41:20.000+1100', '2020-12-31T23:59:59.999+1100'],
        ['year', '2020-01-10T22:41:20.000Z', '2020-12-31T23:59:59.999Z'],
        ['year', '2020-01-10', '2020-12-31'],

        ['month', '2020-02-10T22:41:20.000+1100', '2020-02-29T23:59:59.999+1100'],
        ['month', '2020-09-10T22:41:20.000Z', '2020-09-30T23:59:59.999Z'],
        ['month', '2020-01-01T22:41:20.000Z', '2020-01-31T23:59:59.999Z'],
        ['month', '2020-02-10', '2020-02-29'],

        ['day', '2020-01-10T22:41:20.000+1100', '2020-01-10T23:59:59.999+1100'],
        ['day', '2020-01-10T22:41:20.000Z', '2020-01-10T23:59:59.999Z'],
        ['day', '2020-01-10', '2020-01-10'],

        ['hour', '2020-01-10T22:41:20.000+1100', '2020-01-10T22:59:59.999+1100'],
        ['hour', '2020-01-10T22:41:20.000Z', '2020-01-10T22:59:59.999Z'],
        ['hour', '2020-01-10', '2020-01-10'],

        ['minute', '2020-01-10T22:41:20.000+1100', '2020-01-10T22:41:59.999+1100'],
        ['minute', '2020-01-10T22:41:20.000Z', '2020-01-10T22:41:59.999Z'],
        ['minute', '2020-01-10', '2020-01-10'],

        ['second', '2020-01-10T22:41:20.111+1000', '2020-01-10T22:41:20.999+1000'],
        ['second', '2020-01-10T22:41:20.111Z', '2020-01-10T22:41:20.999Z'],
        ['second', '2020-01-10', '2020-01-10']
    ])('endOf(%p)(%p)', (type, input, expected) => {
        expect(endOf(type)(input)).toBe(expected);
    });

});
